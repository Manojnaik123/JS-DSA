// ! Greedy Algorithms
// These are the classes of algorithms that make locally optimal choices 
// at each step with the hope of finding the global optimal.  

// At every step of the algorithm, we make a choice that looks the best at the movement. 
// To make the choice we need to sort the array so that we can get the next optimal choice quickly. 

// Greedy algorithms do not always give the best solutions. 

// ? Examples;
// Fractional Knapsack, 
// Dijkstra's algorithm, 
// Kruskal's algorithm, 
// Huffman coding and Prim's Algorithm

